    private void izabranoAuto(){
    jComboBox1.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                JComboBox<String> sourceComboBox = (JComboBox<String>) e.getSource();
                String selectedOption = (String) sourceComboBox.getSelectedItem();
                System.out.println("Selected option: " + selectedOption);
                if (jComboBox1.getSelectedIndex()==0) {
                    jTextField7.setText("Selektuj auto");
                } else {
                LocalDateTime from = LocalDateTime.ofInstant(jDateChooser1.getDate().toInstant(), ZoneId.systemDefault());
                LocalDateTime to = LocalDateTime.ofInstant(jDateChooser2.getDate().toInstant(), ZoneId.systemDefault());

                Duration d = Duration.between(from, to);
                if(d.toDays()<1){
                    jTextField7.setText("Selektuj datume hronoloski");
                } else {
                    
                try {
                    String sql = "SELECT * FROM auta WHERE marka = ? AND model = ? AND godiste = ?";
                    conn = DBConnect.uspostaviKonekciju();
                    ps = conn.prepareStatement(sql);
                    String[] auto = jComboBox1.getSelectedItem().toString().split(" | ");

                    ps.setString(1, auto[0]);
                    ps.setString(2, auto[2]);
                    ps.setString(3, auto[4]);
                    rs = ps.executeQuery();
                    if(rs.next()){
                        System.out.println(rs.getString("cijena"));
                        jTextField7.setText(String.format("Ukupna cijena: %d",(d.toDays() * Integer.parseInt(rs.getString("cijena")))));
                }             
                }
                catch (Exception ex) {
                }
                }
                }
            }
        });
    }
    
